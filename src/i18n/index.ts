export type Locale = keyof typeof messages;
export type MessageKey = keyof typeof messages.en;

let languageMode: LanguageMode = "system";

const getDeviceLocale = () => {
  const i18nLocale =
    I18nManager.getConstants?.().localeIdentifier ??
    NativeModules.I18nManager?.getConstants?.().localeIdentifier ??
    NativeModules.I18nManager?.localeIdentifier;
  if (Platform.OS === "ios") {
    const settings =
      NativeModules.SettingsManager?.settings ??
      NativeModules.SettingsManager?.getConstants?.().settings;
    return settings?.AppleLocale ?? settings?.AppleLanguages?.[0] ?? i18nLocale;
  }
  return i18nLocale;
};

const getSystemLocale = (): Locale => {
  const locale = String(getDeviceLocale() ?? "")
    .replace("_", "-")
    .toLowerCase();
  if (locale.startsWith("zh")) return "zh";
  if (locale.startsWith("ja")) return "ja";
  return "en";
};

export const getSystemLanguageMode = (): Exclude<LanguageMode, "system"> =>
  getSystemLocale();

export const setI18nLanguageMode = (mode: LanguageMode) => {
  languageMode = mode;
};

export const getLocale = (): Locale =>
  languageMode === "system" ? getSystemLocale() : languageMode;

export const t = (
  key: MessageKey,
  values?: Record<string, string | number>
) => {
  let message: string = messages[getLocale()][key] ?? messages.en[key];
  if (!values) return message;
  for (const [name, value] of Object.entries(values)) {
    message = message.replace(new RegExp(`\\{${name}\\}`, "g"), String(value));
  }
  return message;
};
